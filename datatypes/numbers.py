myInt = 3
myFloat = 3.5

print(dir(myInt))   ## show all int methods.
print(dir(myFloat)) ## show all float methods.

## Some methods.
print(myInt + 3)                    # return 6
print(myInt - 1)                    # return 2
print(myInt * 2)                    # return 6
print(myInt / 3)                    # return 1
print(myInt + 1.0)                  # return 4.0
print(myInt / 2)                    # return 1.5
print(myInt // 2)                   # return 1
print(myInt % 2)                    # return 1
print(myInt ** 3)                   # return 27

## Concat.
print(int(myInt) + 5)               # return 8